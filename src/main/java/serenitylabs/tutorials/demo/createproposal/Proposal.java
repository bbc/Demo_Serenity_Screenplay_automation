package serenitylabs.tutorials.demo.createproposal;

public class Proposal {

    private String network;
    private String proposalNumber;
    private String title;
    private int proposedEpisodes;
    private int proposedDuration;
    private String proposedDeliveryDate;
    private String shortSynopsis;
    private String workingSupplierNotesPublic;
    private String workingFeedbackNotes;
    private String workingScheduleFeedbackNotes;
    private String workingRiskFeedbackNotes;
    private String workingOtherFeedbackNotes;
    private String futureStatus;

    public String getNetwork() {
        return network;
    }

    public void setNetwork(String network) {
        this.network = network;
    }

    public String getProposalNumber() {
        return proposalNumber;
    }

    public void setProposalNumber(String proposalNumber) {
        this.proposalNumber = proposalNumber;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getProposedEpisodes() {
        return proposedEpisodes;
    }

    public void setProposedEpisodes(int proposedEpisodes) {
        this.proposedEpisodes = proposedEpisodes;
    }

    public int getProposedDuration() {
        return proposedDuration;
    }

    public void setProposedDuration(int proposedDuration) {
        this.proposedDuration = proposedDuration;
    }

    public String getProposedDeliveryDate() {
        return proposedDeliveryDate;
    }

    public void setProposedDeliveryDate(String proposedDeliveryDate) {
        this.proposedDeliveryDate = proposedDeliveryDate;
    }

    public String getShortSynopsis() {
        return shortSynopsis;
    }

    public void setShortSynopsis(String shortSynopsis) {
        this.shortSynopsis = shortSynopsis;
    }

    public String getWorkingSupplierNotesPublic() {
        return workingSupplierNotesPublic;
    }

    public void setWorkingSupplierNotesPublic(String workingSupplierNotesPublic) {
        this.workingSupplierNotesPublic = workingSupplierNotesPublic;
    }

    public String getWorkingFeedbackNotes() {
        return workingFeedbackNotes;
    }

    public void setWorkingFeedbackNotes(String workingFeedbackNotes) {
        this.workingFeedbackNotes = workingFeedbackNotes;
    }

    public String getWorkingScheduleFeedbackNotes() {
        return workingScheduleFeedbackNotes;
    }

    public void setWorkingScheduleFeedbackNotes(String workingScheduleFeedbackNotes) {
        this.workingScheduleFeedbackNotes = workingScheduleFeedbackNotes;
    }

    public String getWorkingRiskFeedbackNotes() {
        return workingRiskFeedbackNotes;
    }

    public void setWorkingRiskFeedbackNotes(String workingRiskFeedbackNotes) {
        this.workingRiskFeedbackNotes = workingRiskFeedbackNotes;
    }

    public String getWorkingOtherFeedbackNotes() {
        return workingOtherFeedbackNotes;
    }

    public void setWorkingOtherFeedbackNotes(String workingOtherFeedbackNotes) {
        this.workingOtherFeedbackNotes = workingOtherFeedbackNotes;
    }

    public String getFutureStatus() {
        return futureStatus;
    }

    public void setFutureStatus(String futureStatus) {
        this.futureStatus = futureStatus;
    }
}
